def main():
    print(f"=== 兵书数据导入 ({len(BOOKS)} 条) ===\n")

    conn = psycopg2.connect(**DB)
    cur = conn.cursor()
    cur.execute("TRUNCATE TABLE military_books RESTART IDENTITY")
    conn.commit()

    inserted = 0
    for name, system, btype, desc in BOOKS:
        try:
            cur.execute("SAVEPOINT sp1")
            cur.execute("""
                INSERT INTO military_books (name, book_system, book_type, description)
                VALUES (%s, %s, %s, %s)
                ON CONFLICT (name) DO UPDATE SET
                    book_system=EXCLUDED.book_system,
                    book_type=EXCLUDED.book_type,
                    description=EXCLUDED.description
            """, (name, system, btype, desc))
            cur.execute("RELEASE SAVEPOINT sp1")
            inserted += 1
        except Exception as e:
            cur.execute("ROLLBACK TO SAVEPOINT sp1")
            print(f"  ⚠️ 跳过 {name}: {e}")

    conn.commit()
    cur.close()

    # 验证
    cur = conn.cursor()
    cur.execute("SELECT book_system, book_type, COUNT(*) FROM military_books GROUP BY book_system, book_type ORDER BY book_system, book_type")
    print(f"{'系列':<8} {'类型':<8} {'数量'}")
    print("-" * 24)
    for row in cur.fetchall():
        print(f"{row[0]:<8} {row[1]:<8} {row[2]}")
    cur.execute("SELECT COUNT(*) FROM military_books")
    print(f"\n合计：{cur.fetchone()[0]} 条")
    cur.close()
    conn.close()
    print("\n✅ 完成！")


if __name__ == "__main__":
    main()
